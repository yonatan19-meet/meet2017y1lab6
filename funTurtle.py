import turtle
turtle.shape('square')
square = turtle.clone()
square.stamp()
square.goto(100,0)
square.stamp()
square.goto(100,100)
square.stamp()
square.goto(0,100)
square.stamp()
square.goto(0,0)
square.penup()
square.goto(-200,100)
square.shape('triangle')
tri = square.clone()
tri.stamp()
tri.penup()
tri.goto(-150, 13.4)
tri.pendown()
tri.stamp()
tri.goto(-100,100)
tri.stamp()
tri.goto(-200,100)
tri.stamp()
tri.goto(-150,13.4)
turtle.mainloop()
